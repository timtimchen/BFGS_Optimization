function [df] = D_Osborne_1(x)
% This function performs the gradient of Osborne 1 function. n = 5, m = 33. 

    y = [0.844; 0.908; 0.932; 0.936; 0.925; 0.908; 0.881; 0.850; 0.818; 0.784; 0.751; 0.718; 0.685; 0.658; 0.628; 0.603; 0.580; 0.558; 0.538; 0.522; 0.506; 0.490; 0.478; 0.467; 0.457; 0.448; 0.438; 0.431; 0.424; 0.420; 0.414; 0.411; 0.406];
    df = zeros(5,1);
    for i = 1:33
        t = 10*(i-1);
        df(1) = df(1) - 2*(y(i)-(x(1)+x(2)*exp(-t*x(4))+x(3)*exp(-t*x(5))));
        df(2) = df(2) - 2*exp(-t*x(4))*(y(i)-(x(1)+x(2)*exp(-t*x(4))+x(3)*exp(-t*x(5))));
        df(3) = df(3) - 2*exp(-t*x(5))*(y(i)-(x(1)+x(2)*exp(-t*x(4))+x(3)*exp(-t*x(5))));
        df(4) = df(4) + 2*t*x(2)*exp(-t*x(4))*(y(i)-(x(1)+x(2)*exp(-t*x(4))+x(3)*exp(-t*x(5))));
        df(5) = df(5) + 2*t*x(3)*exp(-t*x(5))*(y(i)-(x(1)+x(2)*exp(-t*x(4))+x(3)*exp(-t*x(5))));
    end

end