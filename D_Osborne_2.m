function [df] = D_Osborne_2(x)
% This function performs the gradient of Osborne 1 function. n = 11, m = 65. 

    y = [1.366; 1.191; 1.112; 1.013; 0.991; 0.885; 0.831; 0.847; 0.786; 0.725;...
        0.746; 0.679; 0.608; 0.655; 0.616; 0.606; 0.602; 0.626; 0.651; 0.724;...
        0.649; 0.649; 0.694; 0.644; 0.624; 0.661; 0.612; 0.558; 0.533; 0.495;...
        0.500; 0.423; 0.395; 0.375; 0.372; 0.391; 0.396; 0.405; 0.428; 0.429;...
        0.523; 0.562; 0.607; 0.653; 0.672; 0.708; 0.633; 0.668; 0.645; 0.632;...
        0.591; 0.559; 0.597; 0.625; 0.739; 0.710; 0.729; 0.720; 0.636; 0.581;...
        0.428; 0.292; 0.162; 0.098; 0.054];
    df = zeros(11,1);
    for i = 1:65
        t = (i-1)/10;
        f_i = y(i)-(x(1)*exp(-t*x(5))+x(2)*exp(-(t-x(9))^2*x(6))+x(3)*exp(-(t-x(10))^2*x(7))+x(4)*exp(-(t-x(11))^2*x(8)));
        
        df(1) = df(1) - 2 * f_i * exp(-t*x(5));
        df(2) = df(2) - 2 * f_i * exp(-(t-x(9))^2*x(6));
        df(3) = df(3) - 2 * f_i * exp(-(t-x(10))^2*x(7));
        df(4) = df(4) - 2 * f_i * exp(-(t-x(11))^2*x(8));
        df(5) = df(5) + 2 * f_i * x(1)*exp(-t*x(5)) * t;
        df(6) = df(6) + 2 * f_i * x(2)*exp(-(t-x(9))^2*x(6)) * (t-x(9))^2;
        df(7) = df(7) + 2 * f_i * x(3)*exp(-(t-x(10))^2*x(7)) * (t-x(10))^2;
        df(8) = df(8) + 2 * f_i * x(4)*exp(-(t-x(11))^2*x(8)) * (t-x(11))^2;
        df(9) = df(9) - 4 * f_i * x(2)*exp(-(t-x(9))^2*x(6)) * x(6) * (t-x(9));
        df(10) = df(10) - 4 * f_i * x(3)*exp(-(t-x(10))^2*x(7)) * x(7) * (t-x(10));
        df(11) = df(11) - 4 * f_i * x(4)*exp(-(t-x(11))^2*x(8)) * x(8) * (t-x(11));
    end

end